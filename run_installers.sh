#!/bin/bash

# Скрипт для запуска установщиков

echo "==========================================="
echo "        УСТАНОВКА ПРИЛОЖЕНИЯ"
echo "==========================================="

echo "Доступные установщики:"
echo "1. Графический установщик (GUI)"
echo "2. Консольный установщик"
echo "3. Выход"
echo

read -p "Выберите установщик (1-3): " choice

case $choice in
    1)
        echo "Запуск графического установщика..."
        echo "Примечание: для запуска требуется система с графическим интерфейсом и Tkinter"
        python3 /workspace/installer_demo.py
        ;;
    2)
        echo "Запуск консольного установщика..."
        python3 /workspace/console_installer.py
        ;;
    3)
        echo "Выход."
        exit 0
        ;;
    *)
        echo "Неверный выбор. Пожалуйста, выберите 1, 2 или 3."
        exit 1
        ;;
esac